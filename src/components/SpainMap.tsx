export default function SpainMap({ auctionCounts }: SpainMapProps) {
  const router = useRouter();
  const [hovered, setHovered] = useState<string | null>(null);

  const maxCount = useMemo(() => {
    const vals = Object.values(auctionCounts).filter(v => v > 0);
    return vals.length > 0 ? Math.max(...vals) : 1;
  }, [auctionCounts]);

  const getColor = (count: number) => {
    if (count === 0) return '#1e293b';
    const r = count / maxCount;
    if (r > 0.6) return '#7c3aed';
    if (r > 0.3) return '#5b21b6';
    return '#3b0764';
  };

  const getStroke = (count: number) => {
    if (count === 0) return '#334155';
    const r = count / maxCount;
    if (r > 0.6) return '#a78bfa';
    if (r > 0.3) return '#8b5cf6';
    return '#6d28d9';
  };

  return (
    <div className="relative w-full max-w-2xl mx-auto">
      <svg viewBox="0 0 400 300" className="w-full" style={{ minHeight: '240px' }}>
        {Object.entries(PROVINCE_PATHS).map(([name, path]) => {
          const count = auctionCounts[name] || 0;
          const isH = hovered === name;
          return (
            <path
              key={name}
              d={path}
              fill={isH ? getStroke(count) : getColor(count)}
              stroke={isH ? '#e2e8f0' : getStroke(count)}
              strokeWidth={isH ? 1.5 : 0.5}
              style={{ cursor: 'pointer', transition: 'fill 0.15s, stroke 0.15s' }}
              onClick={() => router.push(`/buscar?provincia=${encodeURIComponent(name)}`)}
              onMouseEnter={() => setHovered(name)}
              onMouseLeave={() => setHovered(null)}
            />
          );
        })}

        {(() => {
          const count = auctionCounts['Baleares'] || 0;
          const isH = hovered === 'Baleares';
          return (
            <path
              d={BALEARES_PATH}
              fill={isH ? getStroke(count) : getColor(count)}
              stroke={isH ? '#e2e8f0' : getStroke(count)}
              strokeWidth={isH ? 1.5 : 0.5}
              style={{ cursor: 'pointer', transition: 'fill 0.15s' }}
              onClick={() => router.push('/buscar?provincia=Baleares')}
              onMouseEnter={() => setHovered('Baleares')}
              onMouseLeave={() => setHovered(null)}
            />
          );
        })()}

        <rect x="48" y="255" width="65" height="38" rx="3" fill="none" stroke="#334155" strokeWidth="0.5" strokeDasharray="3,2" />
        <text x="80" y="253" textAnchor="middle" fill="#475569" fontSize="6">Canarias</text>

        {hovered && CENTROIDS[hovered] && (() => {
          const c = CENTROIDS[hovered];
          const count = auctionCounts[hovered] || 0;
          const tx = Math.min(Math.max(c.x, 60), 340);
          const ty = c.y - 20;
          return (
            <g>
              <rect x={tx - 55} y={ty - 9} width="110" height="18" rx="3" fill="#0f172a" stroke="#475569" strokeWidth="0.5" />
              <text x={tx} y={ty + 4} textAnchor="middle" fill="#e2e8f0" fontSize="8" fontWeight="600">
                {hovered}: {count} subastas
              </text>
            </g>
          );
        })()}
      </svg>

      <div className="flex justify-center gap-4 mt-1 text-xs text-slate-500">
        <span className="flex items-center gap-1"><span className="w-3 h-3 rounded-sm bg-gray-800 inline-block"></span> Sin datos</span>
        <span className="flex items-center gap-1"><span className="w-3 h-3 rounded-sm bg-violet-900 inline-block"></span> Pocas</span>
        <span className="flex items-center gap-1"><span className="w-3 h-3 rounded-sm bg-violet-600 inline-block"></span> Muchas</span>
      </div>
    </div>
  );
}
